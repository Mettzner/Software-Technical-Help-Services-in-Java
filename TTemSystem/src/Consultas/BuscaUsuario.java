    public void limparTabela(){
        DefaultTableModel dtm = (DefaultTableModel)TTabela.getModel();
        for(int i=0;i<TTabela.getRowCount();i++){
            dtm.removeRow(0);
        }
    }
    public void buscarAutomatico(){
        limparTabela();
        limparTabela();
        List lista = usuariodao.buscarContatos(TNome.getText());
        
        for(int i=0; i<lista.size();i++){
            UsuarioPOJO usuariopojo = (UsuarioPOJO)lista.get(i);
            DefaultTableModel dtm = (DefaultTableModel)TTabela.getModel();
            dtm.addRow(new Object[]{usuariopojo.getCODIGOU(),
                usuariopojo.getNOME(), usuariopojo.getUSUARIO(),
                usuariopojo.getSENHA(), usuariopojo.getACESSO()});
        }
    }
