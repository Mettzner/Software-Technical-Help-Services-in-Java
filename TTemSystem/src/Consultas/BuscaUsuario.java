    private void BDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BDadosActionPerformed
    
        if(TTabela.getSelectedRow()<0){
            JOptionPane.showMessageDialog(null, "É necessário selecionar um registro");
        }else{
            int linha=TTabela.getSelectedRow();
            Object dado=TTabela.getValueAt(linha, 0);
            int CODIGO=Integer.parseInt(""+dado);
            usuariopojo=usuariodao.buscarContatoUnico(CODIGO);
            
            JOptionPane.showMessageDialog(null,
                    "Codigo "+ usuariopojo.getCODIGOU()+"\n"
                    +"Nome "+usuariopojo.getNOME()+"\n"
                    +"Nivel de Acesso"+usuariopojo.getACESSO()+"\n"
                    +"Usuario"+usuariopojo.getUSUARIO()+"\n"
                    +"Senha"+usuariopojo.getSENHA()+"\n");
        }
    }//GEN-LAST:event_BDadosActionPerformed

    private void BExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BExcluirActionPerformed
    
        if(TTabela.getSelectedRow()<0){
            JOptionPane.showMessageDialog(null, "É obrigatório selecionar uma linha");
        }else{
            int linha=TTabela.getSelectedRow();
            int CODIGO=Integer.parseInt(""+TTabela.getValueAt(linha, 0));
            String NOME=""+TTabela.getValueAt(linha, 1);
            int ok=JOptionPane.showConfirmDialog(null, "Deseja Realmente"
                    +"excluir o contato do(a)"+NOME);
            if(ok==0){
               // usuariodao.removerContato(CODIGO);
                limparTabela();
                buscarAutomatico();
            }
        }
    }//GEN-LAST:event_BExcluirActionPerformed

